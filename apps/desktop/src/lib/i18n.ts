export function t(locale: Locale, key: MessageKey): string {
  return messages[locale][key];
}

export function translateCategoryName(locale: Locale, categoryId: string, fallback: string): string {
  return categoryNames[categoryId]?.[locale] ?? fallback;
}

export function translateCategoryFromScan(locale: Locale, category: CleanupScanResult | CleanupExecutionResult): string {
  return translateCategoryName(locale, category.categoryId, category.displayName);
}

